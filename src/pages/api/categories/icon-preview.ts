/**
 * Category Icon Preview API
 *
 * Returns HTML for a category icon preview.
 * Used by the CategoryModal to dynamically update the icon preview
 * when the user selects a different icon or color.
 *
 * GET /api/categories/icon-preview?icon=house&color=bg-primary
 */

import type { APIContext } from 'astro';
import { isIconSupported } from '@/lib/utils/supportedCategoryIcons';

// Valid color classes
const VALID_COLORS = [
  'bg-primary',
  'bg-secondary',
  'bg-accent',
  'bg-neutral',
  'bg-success',
  'bg-warning',
  'bg-error',
  'bg-info',
];

export async function GET(context: APIContext): Promise<Response> {
  const url = new URL(context.request.url);
  const icon = url.searchParams.get('icon') || 'tag';
  const color = url.searchParams.get('color') || 'bg-primary';

  // Validate icon (security: prevent arbitrary icon names)
  const safeIcon = isIconSupported(icon) ? icon : 'tag';

  // Validate color (security: prevent arbitrary class names)
  const safeColor = VALID_COLORS.includes(color) ? color : 'bg-primary';

  // Convert kebab-case to PascalCase for Lucide icon lookup
  const iconName = safeIcon
    .split('-')
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join('');

  // Generate the icon HTML using inline SVG from Lucide
  // This is a simplified approach - for production, consider using a proper icon component
  const html = `
<div class="w-10 h-10 rounded-xl ${safeColor} flex items-center justify-center text-white shadow-lg transition-transform">
  <svg xmlns="http://www.w3.org/2000/svg" width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" class="stroke-current" aria-hidden="true">
    ${getIconPath(iconName)}
  </svg>
</div>
  `.trim();

  return new Response(html, {
    status: 200,
    headers: {
      'Content-Type': 'text/html',
      'Cache-Control': 'public, max-age=3600', // Cache for 1 hour
    },
  });
}

